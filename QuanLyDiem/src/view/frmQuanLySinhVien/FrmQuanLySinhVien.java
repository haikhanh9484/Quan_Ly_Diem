    private void LoadChiTietSV() {
        int masv = Integer.parseInt(tblSinhVien.getValueAt(selectedRow, 0).toString());
        dstichLuy = SinhVienDAO.getInstance().layDiemTichLuy(masv);
        dsKQ = SinhVienDAO.getInstance().layKQHocTap(masv);
        tblDiemTBHocKy.setModel(new CustomTableTichLuy(dstichLuy));
        tblKQHocTap.setModel(new CustomTableDiem(dsKQ));

        float soTin = 0;
        float diemTB = 0;
        for (DiemTichLuy d : dstichLuy) {
            soTin += d.getTongTinChi();
            diemTB += d.getTbcHocKy() * d.getTongTinChi();
        }
        
        float diemTichLuy = diemTB / soTin;
        
        DecimalFormat df = new DecimalFormat();
        df.setMaximumFractionDigits(2);
        if(soTin == 0){
            diemTichLuy = 0;
        }
        txtDiemTBCTichLuy.setText("Điểm trung bình chung tích lũy: " + df.format(diemTichLuy));

        for (int i = 0; i < tblKQHocTap.getColumnCount(); i++) {
            tblKQHocTap.getTableHeader().getColumnModel().getColumn(i).setHeaderRenderer(customHeaderCellRendered);
            tblKQHocTap.getTableHeader().getColumnModel().getColumn(i).setCellRenderer(new DefaultTableCellRenderer() {
                @Override
                public void setHorizontalAlignment(int alignment) {
                    super.setHorizontalAlignment(CENTER); //To change body of generated methods, choose Tools | Templates.
                }
            });
        }
        for (int i = 0; i < tblDiemTBHocKy.getColumnCount(); i++) {
            tblDiemTBHocKy.getTableHeader().getColumnModel().getColumn(i).setHeaderRenderer(customHeaderCellRendered);
            tblDiemTBHocKy.getTableHeader().getColumnModel().getColumn(i).setCellRenderer(new DefaultTableCellRenderer() {
                @Override
                public void setHorizontalAlignment(int alignment) {
                    super.setHorizontalAlignment(CENTER); //To change body of generated methods, choose Tools | Templates.
                }
            });
        }
    }

