    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        try {
            selectedRow = tblMonHoc.getSelectedRow();
            if (selectedRow < 0 || selectedRow > dsMon.size()) {
                throw new Exception("Vui lòng chọn bản ghi cần xóa!");
            }
            Mon mon = dsMon.get(selectedRow);
            int chose = JOptionPane.showConfirmDialog(rootPane, "Bạn có thực sự muốn xóa môn " + mon.getTenMon(), "Xác nhận xóa", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (chose == JOptionPane.YES_OPTION) {
                if (DiemDAO.getInstance().kiemTraMonCoDiem(dsMon.get(selectedRow))) {
                    JOptionPane.showMessageDialog(rootPane, "Không thể xóa môn học đã nhập điểm!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                if (Controller.getInstance().xoa(mon)) {
                    JOptionPane.showMessageDialog(null, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    laySoTrang();
                    layDsTheoTrang(trangHienTai);
                    loadDuLieu();
                    dangTimKiem = false;
                    txtTuKhoa.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Xóa thất bại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnXoaMouseClicked

    private void btnTimKiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTimKiemMouseClicked
        try {
            dangTimKiem = true;
            tuKhoa = txtTuKhoa.getText().trim();
            if (tuKhoa.equalsIgnoreCase("")) {
                throw new Exception("Vui lòng nhập từ khóa cần tìm kiếm!");
            }
            tongTrang = MonDAO.getInstance().layTongSoBanGhiTimKiem(tuKhoa);
            if (tongTrang == 0) {
                throw new Exception("Không tìm thấy dữ liệu trong bảng");
            }
            tongTrang = tongTrang % 15 == 0 ? tongTrang / 15 : tongTrang / 15 + 1;
            trangHienTai = 1;
            getOldData();
            dsMon = MonDAO.getInstance().layDSMonTheoTrangTimKiem(trangHienTai, tuKhoa);
            setClickState();
            loadDuLieu();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTimKiemMouseClicked

