    private void btnSinhLopDocLapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSinhLopDocLapMouseClicked
        dsMonCbb = MonDAO.getInstance().layDSMon();
        String[] dsTenMon = new String[dsMonCbb.size()];
        int i = 0;
        for (Mon mon : dsMonCbb) {
            dsTenMon[i] = mon.getTenMon();
            i++;
        }
        DefaultComboBoxModel cbbMonModel = new DefaultComboBoxModel<>(dsTenMon);
        cbbTenMon.setModel(cbbMonModel);
        selectedRow = tblMonHoc.getSelectedRow();
        if (selectedRow >= 0 && selectedRow < dsMon.size()) {
            Mon mon = dsMon.get(selectedRow);
            for (int j = 0; j < dsMonCbb.size(); j++) {
                if (dsMonCbb.get(j).getMaMon() == mon.getMaMon()) {
                    cbbTenMon.setSelectedIndex(j);
                }
            }
        }
        laySoLopDaTao();
        dialogSinhLopDocLap.setVisible(true);
    }//GEN-LAST:event_btnSinhLopDocLapMouseClicked

