    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        try {
            selectedRow = tblSinhVien.getSelectedRow();
            if (selectedRow < 0 || selectedRow > dsSinhVien.size()) {
                throw new Exception("Vui lòng chọn bản ghi cần xóa!");
            }
            SinhVien sv = dsSinhVien.get(selectedRow);
            int chose = JOptionPane.showConfirmDialog(rootPane, "Bạn có thực sự muốn xóa sinh viên: " + sv.getTenSinhVien(), "Xác nhận xóa", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (chose == JOptionPane.YES_OPTION) {
                if (SinhVienDAO.getInstance().checkSinhVienCoDiem(sv.getMaSinhVien())) {
                    JOptionPane.showMessageDialog(null, "Không được phép xóa sinh viên đã có điểm!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else if (Controller.getInstance().xoa(sv)) {
                    JOptionPane.showMessageDialog(null, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    laySoTrang();
                    if (tongTrang < trangHienTai) {
                        trangHienTai = tongTrang;
                    }
                    layDsTheoTrang(trangHienTai);
                    loadDuLieu();
                    dangTimKiem = false;
                    txtTuKhoa.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Xóa thất bại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnXoaMouseClicked

