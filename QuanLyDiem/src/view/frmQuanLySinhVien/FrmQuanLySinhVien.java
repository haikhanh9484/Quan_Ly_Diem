    private void btnSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMouseClicked
        try {
            selectedRow = tblSinhVien.getSelectedRow();
            if (selectedRow < 0 || selectedRow > dsSinhVien.size()) {
                throw new Exception("Vui lòng chọn bản ghi cần sửa!");
            }
            SinhVien sinhVien = dsSinhVien.get(selectedRow);
            txtMaSinhVien.setText(sinhVien.getMaSinhVien() + "");
            txtTenSinhVien.setText(sinhVien.getTenSinhVien());
            txtDiaChi.setText(sinhVien.getDiaChi());
            cobLop.setSelectedItem(sinhVien.getLop().getTenLop());
            dchNgaySinh.setDate(sinhVien.getNgaySinh());
            if (sinhVien.getGioiTinh().equalsIgnoreCase("Nam")) {
                radNam.setSelected(true);
            } else {
                radNu.setSelected(true);
            }
            cobTrangThai.setSelectedIndex(sinhVien.getTrangThai());
            anhSinhVien = sinhVien.getAnh();
            if (anhSinhVien != null) {
                lblAnh.setText("");
                ImageIcon icon = new ImageIcon(new ImageIcon(anhSinhVien).getImage().getScaledInstance(widthImage, heightImage, Image.SCALE_SMOOTH));
                lblAnh.setIcon(icon);
            } else {
                lblAnh.setText("Nhấp chọn hình ảnh...");
                lblAnh.setIcon(null);
            }
            btnDieuKhien.setIcon(iconSua);
            dialogType = false;
            dialogSinhVien.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaMouseClicked

