    private void hoverBtn(JPanel pn, boolean b) {
        if (b) {
            this.setCursor(handleMouse);
            Thread thread = new Thread() {
                @Override
                public void run() {
                    try {
                        for (int i = 0; i <= 5; i++) {
                            Thread.sleep(3);
                            pn.setLocation(pn.getX(), pn.getY() - 1);
                        }
                    } catch (InterruptedException e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }
                }
            };
            thread.start();
            pn.setBackground(colorHoverMenu);
        } else {
            this.setCursor(defaultMouse);
            Thread thread = new Thread() {
                @Override
                public void run() {
                    try {
                        for (int i = 5; i >= 0; i--) {
                            Thread.sleep(3);
                            pn.setLocation(pn.getX(), pn.getY() + 1);
                        }
                    } catch (InterruptedException e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }
                }
            };
            thread.start();
            pn.setBackground(colorMenu);
        }
    }

    private void themSuaMon() {
        try {
            if (dialogType) {
                Mon mon = new Mon();
                mon.setMaMon(Integer.parseInt(txtMaMon.getText().trim()));
                mon.setTenMon(txtTenMon.getText().trim());
                String strTinChi = txtSoTin.getText().trim();
                if (strTinChi.equals("")) {
                    throw new Exception("Số tín chỉ không được rỗng");
                }
                mon.setSoTin(Integer.parseInt(strTinChi));
                if (Controller.getInstance().themMoi(mon)) {
                    JOptionPane.showMessageDialog(null, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    laySoTrang();
                    layDsTheoTrang(trangHienTai);
                    loadDuLieu();
                    dangTimKiem = false;
                    txtTuKhoa.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Thêm thất bại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
                dialogMonHoc.setVisible(false);
            } else {
                Mon mon = dsMon.get(selectedRow);
                mon.setMaMon(Integer.parseInt(txtMaMon.getText().trim()));
                mon.setTenMon(txtTenMon.getText().trim());
                String strTinChi = txtSoTin.getText().trim();
                if (strTinChi.equals("")) {
                    throw new Exception("Số tín chỉ không được rỗng");
                }
                mon.setSoTin(Integer.parseInt(strTinChi));
                if (Controller.getInstance().capNhat(mon)) {
                    JOptionPane.showMessageDialog(null, "Sửa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    laySoTrang();
                    layDsTheoTrang(trangHienTai);
                    loadDuLieu();
                    dangTimKiem = false;
                    txtTuKhoa.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Sửa thất bại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
                dialogMonHoc.setVisible(false);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Số tín chỉ phải là số nguyên dương!", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

